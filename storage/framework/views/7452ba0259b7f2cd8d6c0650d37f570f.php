<script>
    function toggleFridaySearch() {
        const wrapper = document.getElementById('fridaySearchWrapper');
        const input = document.getElementById('fridaySearchInput');
        if (!wrapper.classList.contains('active')) {
            wrapper.classList.add('active');
            input.focus();
        } else if (input.value.trim() !== "") {
            wrapper.querySelector('form').submit();
        } else {
            wrapper.classList.remove('active');
        }
    }

